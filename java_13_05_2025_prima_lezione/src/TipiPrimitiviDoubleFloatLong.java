public class TipiPrimitiviDoubleFloatLong {
    public static void main(String[] args) {
        //! il dobule d/D
        double myDouble = 3.14D;
        System.out.println(myDouble);

        //! il float f/F
        float myFloat = 3.14F;
        System.out.println(myFloat);

        //! il long l/L
        long myLong = 123456789L;
        System.out.println(myLong);
    }
    
}
